import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.css";

import Header from "@/components/header";
import ProgressBar from "@/components/ui/progress-bar";
import { InitialShow } from "@/components/motion";
import { Toaster } from "react-hot-toast";
import { SessionProvider } from "next-auth/react";

import Footer from "@/components/footer";
import { getServerSession } from "next-auth";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({ children }: { children: React.ReactNode }) {
  const session = await getServerSession();
  return (
    <>
      <html lang="en">
        <SessionProvider session={session}>
          <body
            className={`${inter.className} min-h-dvh w-full overflow-hidden scrollbar [overflow-y:overlay] [scrollbar-width:none]`}
          >
            <ProgressBar />
            <Header />
            <InitialShow>
              <main>{children}</main>
            </InitialShow>

            <Toaster
              position="top-center"
              toastOptions={{
                className: "!bg-background border border-white/10 !text-primary",
              }}
            />

            <Footer />
          </body>
        </SessionProvider>
      </html>
    </>
  );
}
